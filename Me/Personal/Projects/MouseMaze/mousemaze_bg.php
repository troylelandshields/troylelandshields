			<p class="resume-bg">		
				<a target="_blank" href="http://troylelandshields.me/MouseRace.aspx">
				Mouse Maze</a> was an entertaining way for me to learn C#. I had gotten a job
				with HydroDesktop, but had very little experience programming outside of 
				school and no experience with C#. Using the two weeks I had before my first
				day with HydroDesktop, I built and published this Mouse Maze game.
				<br>
				<br>
				Mouse Maze is 10 levels of frustrating awesome. You start by giving your name
				and an optional hashtag (if you want to compete for best time among a specific
				group of hashtag users, for example) and then the timer starts as you race up
				the 10 levels.
				<br><br>
				I was originally thinking it might be interesting to collect data about what 
				mouse/operating system people were using and how that correlated with their time.
				Although I collect this data when you start the race, I have yet to do anything
				with it.
				<br><br>
				PS: Mouse Maze runs on Silverlight, which has sub-par performance on Chrome for Mac OS X.
			</p>
