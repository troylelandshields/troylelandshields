			<p class="resume-bg">		
				PrestoPresento is the marriage of two open-source Javascript libraries and
				some cleverness from myself. I use <a href="http://arborjs.org" target="_blank">arborjs</a>
				and <a href="http://nyromodal.nyrodev.com" target="_blank">nyroModal</a> to display nodes
				and modal lightboxes. 
				<br>
				<br>
				The real genius comes from the PHP code that traverses the folder structure
				to build the nodes though. I simply tell PrestoPresento the folder in which
				to start, and it builds the nodes and links based on the folders and files
				that it finds inside.
				<br>
				<br>
				I created this because I needed to make a presentation and absolutely did 
				not want to use Powerpoint. Powerpoint is boring and linear, and I wanted 
				my presentation to overcome at least one of those things. By using PrestoPresento,
				I could go through my speech linearly, but display my ideas at the same 
				time. The result being that the first thing I talk about is still on the screen (and still
			 	in the audience's mind) by the time I finish up with my last point.
			 	<br>
			 	<br>
			 	After that I realized two other uses of PrestoPresento. First is as
			 	<a href="http://shldz.us/travels" target="_blank">a photo travelogue on shldz.us.</a>
			 	Second is an "interactive resume" for myself (which is what this is).
			 	<br>
			 	<br>
			 	Next on the to-do list for PrestoPresento: come up with a new name.
